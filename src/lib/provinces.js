export const getAllProvinces = () => {
  let all = [];
  regions.forEach(r => {
    r.provinces.forEach(p => {
      all.push({ name: p, region: r.name });
    });
  });
  return all;
};

export const getProvinceInfo = (provinceName) => {
  const all = getAllProvinces();
  return all.find(p => p.name === provinceName) || null;
};
